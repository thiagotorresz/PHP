<?php
$c = 5;
$t = 3;

if ($c == $t) {
    echo "$c é igual a $t <br>";
} else {
    echo "$c não é igual a $t <br>";
}

if ($c != $t) {
    echo "$c é diferente de $t <br>";
} else {
    echo "$c não é diferente de $t <br>";
}

if ($c === $t) {
    echo "$c é idêntico a $t <br>";
} else {
    echo "$c não é idêntico a $t <br>";
}

if ($c !== $t) {
    echo "$c é não idêntico a $t <br>";
} else {
    echo "$c não é não idêntico a $t <br>";
}
